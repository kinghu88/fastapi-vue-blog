<template>
  <div class="login" :style="{'background-image': `url(${backgroundImage})`}">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form">
      <p style="margin-bottom: 0;font-size: 22px;color: #b2b2b2;">欢迎到访</p>
      <p style="margin-top: 0;font-size: 25px;color: #474747; margin-bottom: 40px;">博客后台管理系统</p>
      <el-form-item prop="username">
        <el-input v-model="loginForm.username" type="text" auto-complete="off" placeholder="账号">
          <svg-icon slot="prefix" icon-class="user" class="el-input__icon input-icon" />
        </el-input>
      </el-form-item>
      <el-form-item prop="password">
        <el-input
          v-model="loginForm.password"
          type="password"
          auto-complete="off"
          placeholder="密码"
          @keyup.enter.native="handleLogin"
        >
          <svg-icon slot="prefix" icon-class="password" class="el-input__icon input-icon" />
        </el-input>
      </el-form-item>
      <el-form-item style="width:100%;">
        <el-button
          :loading="loading"
          size="medium"
          type="primary"
          style="width:100%;"
          @click.native.prevent="handleLogin"
        >
          <span v-if="!loading">登 录</span>
          <span v-else>登 录 中...</span>
        </el-button>
      </el-form-item>
    </el-form>
    <!--  底部  -->
    <div class="el-login-footer">
      <span>Copyright © 2021 FastAPI-Vue-Blog All Rights Reserved.</span>
    </div>
  </div>
